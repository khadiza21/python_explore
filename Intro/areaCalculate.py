#Triangle area
print("Triangle area")
base=float(input("Enter Base = "))
height = float(input("Enter Height = "))

t_area = 0.5*base*height
print("\nTriangle area is " ,t_area)


#Circle area
print("\n\nCircle area")
radius=float(input("Enter radius = "))

c_area = 3.1416*radius*radius
print("\n Circle area is " ,c_area)

